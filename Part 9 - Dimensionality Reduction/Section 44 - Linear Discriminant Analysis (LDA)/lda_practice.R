# Classification template

# Importing the dataset
dataset = read.csv('Wine.csv')

# Splitting the dataset into the Training set and Test set
# install.packages('caTools')
library(caTools)
set.seed(123)
split = sample.split(dataset$Customer_Segment, SplitRatio = 0.8)
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

# Feature Scaling
training_set[-14] = scale(training_set[-14])
test_set[-14] = scale(test_set[-14])

#Applying LDA
library(MASS)
lda = lda(formula = Customer_Segment ~ .,
          data = training_set) 
training_set = as.data.frame(predict(lda, training_set))
training_set = training_set[c(5,6,1)]
test_set = as.data.frame(predict(lda, test_set))
test_set = test_set[c(5,6,1)]


#Not removing last column here, because it is a supervised 
#dimensionality technique.

#Well it's quite intuitive to understand that the number of linear 
#discriminants will be related to the information of the dependent 
#variable and that information is actually the number of classes in the
#dependent variable. And there is this explicit correlation between the
#number of linear or discriminants and the information of the dependent 
#variable. It's that there will be k minus one linear discriminants 
#where k is the number of classes. So here since we have three classes 
#that means that we'll get at most three minus one equals two linear
#or discriminants. So here without specifying the number of linear 
#discriminants equal to 2 we will get automatically to linear or 
#discriminants and therefore that's all.

# Fitting SVM to the Training set
# install.packages('e1071')
library(e1071)
classifier = svm(formula = class ~ .,
                 data = training_set,
                 type = 'C-classification',
                 kernel = 'linear')

# Predicting the Test set results
y_pred = predict(classifier, newdata = test_set[-3])

# Making the Confusion Matrix
cm = table(test_set[, 3], y_pred)

# Visualising the Training set results
library(ElemStatLearn)
set = training_set
X1 = seq(min(set[, 1]) - 1, max(set[, 1]) + 1, by = 0.01)
X2 = seq(min(set[, 2]) - 1, max(set[, 2]) + 1, by = 0.01)
grid_set = expand.grid(X1, X2)
colnames(grid_set) = c('x.LD1', 'x.LD2')
y_grid = predict(classifier, newdata = grid_set)
plot(set[, -3],
     main = 'SVM (Training set)',
     xlab = 'LD1', ylab = 'LD2',
     xlim = range(X1), ylim = range(X2))
contour(X1, X2, matrix(as.numeric(y_grid), length(X1), length(X2)), add = TRUE)
points(grid_set, pch = '.', col = ifelse(y_grid == 2, 'deepskyblue' ,ifelse(y_grid == 1, 'springgreen3', 'tomato')))
points(set, pch = 21, bg = ifelse(set[, 3] == 2, 'blue3', ifelse(set[, 3]==1, 'green4', 'red3')))

# Visualising the Test set results
